fn main() {
    // addition
    let sum = 5 + 10;
    println!("let sum = 5 + 10;");
    println!("sum = {sum}");

    // substraction
    let difference = 95.3 - 92.5;
    println!("let difference = 95.3 - 92;");
    println!("difference = {difference}");

    // multiplication
    let product = 5 * 10;
    println!("let product = 5 * 10");
    println!("product = {product}");

    //division
    let quotient = 65.2 / 32.2;
    println!("let quotient = 65.2 / 32.2 // not floored");
    println!("quotient : {quotient}");
    let floored = 2/ 3; //Results in 0
    println!("let floored = 2/3;");
    println!("floored = {floored}");

    // remainder
    let remainder= 43 % 5;
    println!("let remainder= 43 % 5;");
    println!("remainder = {remainder}");
}
