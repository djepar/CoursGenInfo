#10.26 list Type Conversion Function
#Python a une fonction intégrée qui tourne tout ce qu'on luit donne en liste : list

xs = list('Crunchy Frod')
print(xs)

#Cette fonction prend tous les éléments et forme une liste(xs dans ce cas-çi)
#Il n'est pas légal d'utiliser la 'list conversion function' pour tout argument qui n'est pas une séquence

#La fonction de conversion list place chaque élément de la séquence original dans une nouvelle liste.
#Lorsqu'elle est utilisée sur des 'strings', le résult est très différent de split
# split déconstruit la 'string' en 'mots
# list déconstruit la string en character. 
